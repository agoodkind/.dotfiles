// SyncRulesFromDir copies .mdc rule files from src into dst as symlinks.
func SyncRulesFromDir(src string, dst string) error {
	if _, err := os.Stat(src); err != nil && !os.IsNotExist(err) {
		slog.Warn("compilation: SyncRulesFromDir stat failed", "src", src, "err", err)
		return fmt.Errorf("stat %s: %w", src, err)
	}
	if sameFile(src, dst) {
		return nil
	}
	if err := os.MkdirAll(dst, 0o755); err != nil {
		return fmt.Errorf("creating directory %s: %w", dst, err)
	}
	entries, err := filepath.Glob(filepath.Join(src, "*.mdc"))
	if err != nil {
		return fmt.Errorf("globbing files in %s: %w", src, err)
	}
	for _, file := range entries {
		target := filepath.Join(dst, filepath.Base(file))
		if err := SyncFile(file, target); err != nil {
			return err
		}
	}
	return nil
}

