const GeneratedCursorMarker = "<!-- Generated by dotfiles from .cursor. Do not edit this file directly. -->"

func CompileZshFiles(ctx context.Context, dotfiles string, logger *telemetry.Logger) error {
	dirs := []string{
		filepath.Join(dotfiles, "zshrc"),
		filepath.Join(dotfiles, "lib", "zinit"),
		filepath.Join(dotfiles, "lib", "zsh-defer"),
		filepath.Join(dotfiles, "home"),
		filepath.Join(dotfiles, "bin"),
	}
	for _, dir := range dirs {
		_ = filepath.WalkDir(dir, func(path string, entry os.DirEntry, err error) error {
			if err != nil {
				return nil
			}
			if entry.IsDir() {
				return nil
			}
			name := filepath.Base(path)
			if !(strings.HasSuffix(name, ".zsh") || name == ".zshrc") {
				return nil
			}
			compiled := path + ".zwc"
			if NeedsCompile(path, compiled) {
				_ = cmdexec.RunWithLogger(ctx, logger, "zsh", "-c", fmt.Sprintf("zcompile %q", path))
			}
			return nil
		})
	}
	return nil
}

func NeedsCompile(source string, target string) bool {
	sourceInfo, err := os.Stat(source)
	if err != nil {
		return false
	}
	targetInfo, err := os.Stat(target)
	if err != nil {
		return true
	}
	return sourceInfo.ModTime().After(targetInfo.ModTime())
}

func RebuildZcompdump(ctx context.Context, dotfiles string, logger *telemetry.Logger) error {
	_ = os.RemoveAll(filepath.Join(os.Getenv("HOME"), ".zcompdump"))
	if !runner.HasCommand("zsh") {
		return nil
	}
	fpath := filepath.Join(dotfiles, "zshrc", "completions")
	return cmdexec.RunWithLogger(ctx, logger, "zsh", "-c", fmt.Sprintf("fpath=(\"%s\" $fpath)\nautoload -Uz compinit\ncompinit -d ~/.zcompdump\nzcompile ~/.zcompdump\n", fpath))
}

func RebuildPreferCache(ctx context.Context, dotfiles string, logger *telemetry.Logger) error {
	return prefercache.Rebuild(ctx, dotfiles, false, logger)
}

func CreateHushLogin(_ context.Context, _ *telemetry.Logger) error {
	path := filepath.Join(os.Getenv("HOME"), ".hushlogin")
	if _, err := os.Stat(path); err == nil {
		return nil
	}
	return common.Touch(path)
}

func SyncRulesFromDir(src string, dst string) error {
	if _, err := os.Stat(src); err != nil {
		return nil
	}
	if err := os.MkdirAll(dst, 0o755); err != nil {
		return err
	}
	entries, err := filepath.Glob(filepath.Join(src, "*.mdc"))
	if err != nil {
		return nil
	}
	for _, file := range entries {
		target := filepath.Join(dst, filepath.Base(file))
		if err := SyncFile(file, target); err != nil {
			return err
		}
	}
	return nil
}

func SyncRulesFromDirAsMd(src string, dst string) error {
	if _, err := os.Stat(src); err != nil {
		return nil
	}
	if err := os.MkdirAll(dst, 0o755); err != nil {
		return err
	}
	entries, err := filepath.Glob(filepath.Join(src, "*.mdc"))
	if err != nil {
		return nil
	}
	for _, file := range entries {
		base := strings.TrimSuffix(filepath.Base(file), filepath.Ext(file))
		target := filepath.Join(dst, base+".md")
		if err := SyncFile(file, target); err != nil {
			return err
		}
	}
	return nil
}

func RenderRulesAsInstructionDoc(src string, dst string, title string) error {
	files, err := sortedFilesWithExt(src, ".mdc")
	if err != nil {
		return nil
	}
	if len(files) == 0 {
		return nil
	}

	var builder strings.Builder
	builder.WriteString("# ")
	builder.WriteString(title)
	builder.WriteString("\n\n")
	builder.WriteString(GeneratedCursorMarker)
	builder.WriteString("\n\n")

	for index, file := range files {
		if index > 0 {
			builder.WriteString("\n\n")
		}
		baseName := strings.TrimSuffix(filepath.Base(file), filepath.Ext(file))
		builder.WriteString("## ")
		builder.WriteString(baseName)
		builder.WriteString("\n\n")

		content, readErr := os.ReadFile(file)
		if readErr != nil {
			return readErr
		}
		body := strings.TrimSpace(stripFrontmatter(string(content)))
		builder.WriteString(body)
		builder.WriteString("\n")
	}

	return writeFileIfChanged(dst, []byte(builder.String()))
}

func SyncCommandFilesAsSkillDirs(src string, dst string, managedPrefix string) error {
	files, err := sortedFilesWithExt(src, ".md")
	if err != nil {
		return nil
	}
	if err := os.MkdirAll(dst, 0o755); err != nil {
		return err
	}

	activeDirs := make(map[string]struct{}, len(files))
	for _, file := range files {
		baseName := strings.TrimSuffix(filepath.Base(file), filepath.Ext(file))
		dirName := managedPrefix + baseName
		activeDirs[dirName] = struct{}{}

		targetDir := filepath.Join(dst, dirName)
		if err := os.MkdirAll(targetDir, 0o755); err != nil {
			return err
		}

		content, readErr := os.ReadFile(file)
		if readErr != nil {
			return readErr
		}
		skillContent := renderCommandAsSkill(baseName, string(content))
		if err := writeFileIfChanged(filepath.Join(targetDir, "SKILL.md"), []byte(skillContent)); err != nil {
			return err
		}
	}

	return removeMissingManagedSkillDirs(dst, managedPrefix, activeDirs)
}

func SyncFilesToDir(src string, dst string) error {
	if _, err := os.Stat(src); err != nil {
		return nil
	}
	if err := os.MkdirAll(dst, 0o755); err != nil {
		return err
	}
	entries, err := os.ReadDir(src)
	if err != nil {
		return nil
	}
	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}
		if err := SyncFile(filepath.Join(src, entry.Name()), filepath.Join(dst, entry.Name())); err != nil {
			return err
		}
	}
	return nil
}

func SyncSkillDirs(src string, dst string) error {
	if _, err := os.Stat(src); err != nil {
		return nil
	}
	if err := os.MkdirAll(dst, 0o755); err != nil {
		return err
	}
	entries, err := os.ReadDir(src)
	if err != nil {
		return nil
	}
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		source := filepath.Join(src, entry.Name())
		target := filepath.Join(dst, entry.Name())
		_ = os.Remove(target)
		if err := os.Symlink(source, target); err != nil {
			return err
		}
	}
	return nil
}

func SyncFile(src string, dst string) error {
	if common.IsSymlinkTo(dst, src) {
		return nil
	}
	_ = os.Remove(dst)
	return os.Symlink(src, dst)
}

func sortedFilesWithExt(src string, ext string) ([]string, error) {
	if _, err := os.Stat(src); err != nil {
		return nil, nil
	}
	files, err := filepath.Glob(filepath.Join(src, "*"+ext))
	if err != nil {
		return nil, err
	}
	sort.Strings(files)
	return files, nil
}

func stripFrontmatter(content string) string {
	if !strings.HasPrefix(content, "---\n") {
		return content
	}
	endMarker := "\n---\n"
	frontmatterEnd := strings.Index(content[4:], endMarker)
	if frontmatterEnd == -1 {
		return content
	}
	return content[4+frontmatterEnd+len(endMarker):]
}

func renderCommandAsSkill(skillName string, body string) string {
	trimmedBody := strings.TrimSpace(body)
	description := fmt.Sprintf(
		"Run the %s workflow from the dotfiles Cursor command library when the user explicitly asks for that command or checklist.",
		skillName,
	)
	return fmt.Sprintf(
		"---\nname: %s\ndescription: %s\n---\n\n%s\n\n%s\n",
		skillName,
		description,
		GeneratedCursorMarker,
		trimmedBody,
	)
}

func writeFileIfChanged(path string, content []byte) error {
	if existing, err := os.ReadFile(path); err == nil {
		if bytes.Equal(existing, content) {
			return nil
		}
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, content, 0o644)
}

func removeMissingManagedSkillDirs(dst string, managedPrefix string, activeDirs map[string]struct{}) error {
	entries, err := os.ReadDir(dst)
	if err != nil {
		return nil
	}
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		if !strings.HasPrefix(entry.Name(), managedPrefix) {
			continue
		}
		if _, ok := activeDirs[entry.Name()]; ok {
			continue
		}
		if err := os.RemoveAll(filepath.Join(dst, entry.Name())); err != nil {
			return err
		}
	}
	return nil
}
