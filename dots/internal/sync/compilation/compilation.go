// SyncFilesToDir copies all files from src into dst as symlinks.
func SyncFilesToDir(src string, dst string) error {
	if _, err := os.Stat(src); err != nil && !os.IsNotExist(err) {
		slog.Warn("compilation: SyncFilesToDir stat failed", "src", src, "err", err)
		return fmt.Errorf("stat %s: %w", src, err)
	}
	if sameFile(src, dst) {
		return nil
	}
	if err := os.MkdirAll(dst, 0o755); err != nil {
		return fmt.Errorf("creating directory %s: %w", dst, err)
	}
	entries, err := os.ReadDir(src)
	if err != nil && !os.IsNotExist(err) {
		slog.Warn("compilation: SyncFilesToDir ReadDir failed", "src", src, "err", err)
		return fmt.Errorf("read dir %s: %w", src, err)
	}
	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}
		if err := SyncFile(filepath.Join(src, entry.Name()), filepath.Join(dst, entry.Name())); err != nil {
			return err
		}
	}
	return nil
}

// SyncSkillDirs copies skill subdirectories from src into dst as symlinks.
func SyncSkillDirs(src string, dst string) error {
	slog.Info("compilation: SyncSkillDirs")
	if _, err := os.Stat(src); err != nil && !os.IsNotExist(err) {
		slog.Warn("compilation: SyncSkillDirs stat failed", "src", src, "err", err)
		return fmt.Errorf("stat %s: %w", src, err)
	}
	if sameFile(src, dst) {
		return nil
	}
	if err := os.MkdirAll(dst, 0o755); err != nil {
		return fmt.Errorf("creating directory %s: %w", dst, err)
	}
	entries, err := os.ReadDir(src)
	if err != nil && !os.IsNotExist(err) {
		slog.Warn("compilation: SyncSkillDirs ReadDir failed", "src", src, "err", err)
		return fmt.Errorf("read dir %s: %w", src, err)
	}
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		source := filepath.Join(src, entry.Name())
		target := filepath.Join(dst, entry.Name())
		if sameFile(source, target) && !isSymlink(target) {
			continue
		}
		if common.IsSymlinkTo(target, source) {
			continue
		}
		_ = os.Remove(target)
		if err := os.Symlink(source, target); err != nil {
			return fmt.Errorf("creating symlink %s: %w", target, err)
		}
	}
	return nil
}

// SyncFile creates a symlink at dst pointing to src, replacing any existing file.
func SyncFile(src string, dst string) error {
	slog.Info("compilation: SyncFile")
	if sameFile(src, dst) && !isSymlink(dst) {
		return nil
	}
	if common.IsSymlinkTo(dst, src) {
		return nil
	}
	_ = os.Remove(dst)
	if err := os.Symlink(src, dst); err != nil {
		slog.Warn("compilation: SyncFile symlink failed", "src", src, "dst", dst, "err", err)
		return fmt.Errorf("creating symlink %s: %w", dst, err)
	}
	return nil
}

func sameFile(firstPath string, secondPath string) bool {
	firstInfo, firstErr := os.Stat(firstPath)
	secondInfo, secondErr := os.Stat(secondPath)
	if firstErr != nil || secondErr != nil {
		return false
	}
	return os.SameFile(firstInfo, secondInfo)
}

func isSymlink(path string) bool {
	info, err := os.Lstat(path)
	if err != nil {
		return false
	}
	return info.Mode()&os.ModeSymlink != 0
}

func sortedFilesWithExt(src string, ext string) ([]string, error) {
	if _, err := os.Stat(src); err != nil && !os.IsNotExist(err) {
		slog.Warn("compilation: sortedFilesWithExt stat failed", "src", src, "err", err)
		return nil, fmt.Errorf("stat %s: %w", src, err)
	}
	files, err := filepath.Glob(filepath.Join(src, "*"+ext))
	if err != nil {
		return nil, fmt.Errorf("globbing files in %s: %w", src, err)
	}
	sort.Strings(files)
	return files, nil
}

func stripFrontmatter(content string) (string, error) {
	_, body, err := parseFrontmatter(content)
	return body, err
}

func renderCommandAsSkill(skillName string, body string) (string, error) {
	trimmedBody := strings.TrimSpace(body)
	description := fmt.Sprintf(
		"Run the /%s command workflow from the dotfiles agent command library when the user explicitly asks for /%s or mentions this command by name.",
		skillName,
		skillName,
	)
	frontmatter, err := renderFrontmatter(frontmatterMetadata{
		Name:        "cursor-command-" + skillName,
		Description: description,
		ApplyTo:     "",
	})
	if err != nil {
		return "", err
	}
	return frontmatter + "\n" + GeneratedAgentHTMLMarker + "\n\n" + trimmedBody + "\n", nil
}

func renderCopilotInstruction(baseName string, frontmatter agentRuleFrontmatter, body string) (string, error) {
	applyTo := strings.TrimSpace(frontmatter.Globs)
	if applyTo == "" && frontmatter.AlwaysApply {
		applyTo = "**/*"
	}

	metadata := frontmatterMetadata{
		Name:        baseName,
		Description: "",
		ApplyTo:     applyTo,
	}
	if description := strings.TrimSpace(frontmatter.Description); description != "" {
		metadata.Description = description
	}
	renderedFrontmatter, err := renderFrontmatter(metadata)
	if err != nil {
		return "", err
	}
	return renderedFrontmatter + "\n" + GeneratedAgentHTMLMarker + "\n\n" + strings.TrimSpace(body) + "\n", nil
}

func renderCopilotPrompt(baseName string, content string) (string, error) {
	frontmatter, body, err := parseFrontmatter(content)
	if err != nil {
		return "", err
	}
	description := strings.TrimSpace(frontmatter.Description)
	if description == "" {
		description = firstMarkdownHeading(body)
	}
	if description == "" {
		description = fmt.Sprintf("Run the %s workflow.", baseName)
	}

	renderedFrontmatter, renderErr := renderFrontmatter(frontmatterMetadata{
		Name:        baseName,
		Description: description,
		ApplyTo:     "",
	})
	if renderErr != nil {
		return "", renderErr
	}
	return renderedFrontmatter + "\n" + GeneratedAgentHTMLMarker + "\n\n" + strings.TrimSpace(body) + "\n", nil
}

func parseFrontmatter(content string) (agentRuleFrontmatter, string, error) {
	var values agentRuleFrontmatter
	if !strings.HasPrefix(content, "---\n") {
		return values, content, nil
	}
	endMarker := "\n---\n"
	frontmatterEnd := strings.Index(content[4:], endMarker)
	if frontmatterEnd == -1 {
		return values, content, nil
	}

	rawFrontmatter := content[4 : 4+frontmatterEnd]
	if err := yaml.Unmarshal([]byte(rawFrontmatter), &values); err != nil {
		slog.Warn("compilation: parseFrontmatter yaml failed", "err", err)
		return values, content, fmt.Errorf("parsing frontmatter: %w", err)
	}

	bodyStart := 4 + frontmatterEnd + len(endMarker)
	return values, content[bodyStart:], nil
}

func firstMarkdownHeading(content string) string {
	for line := range strings.SplitSeq(content, "\n") {
		trimmedLine := strings.TrimSpace(line)
		if !strings.HasPrefix(trimmedLine, "#") {
			continue
		}
		return strings.TrimSpace(strings.TrimLeft(trimmedLine, "#"))
	}
	return ""
}

func renderFrontmatter(metadata frontmatterMetadata) (string, error) {
	content, err := yaml.Marshal(metadata)
	if err != nil {
		slog.Warn("compilation: renderFrontmatter yaml failed", "err", err)
		return "", fmt.Errorf("marshaling frontmatter: %w", err)
	}
	return "---\n" + string(content) + "---\n", nil
}

func writeFileIfChanged(path string, content []byte) error {
	cleanPath := filepath.Clean(path)
	slog.Info("compilation: writeFileIfChanged")
	if existing, err := os.ReadFile(cleanPath); err == nil {
		if bytes.Equal(existing, content) {
			return nil
		}
		if HasGeneratedMarker(string(content)) && !HasGeneratedMarker(string(existing)) && strings.TrimSpace(string(existing)) != "" {
			return nil
		}
	}
	if err := os.MkdirAll(filepath.Dir(cleanPath), 0o755); err != nil {
		slog.Warn("compilation: writeFileIfChanged mkdir failed", "path", path, "err", err)
		return fmt.Errorf("creating directory %s: %w", filepath.Dir(cleanPath), err)
	}
	if err := os.WriteFile(cleanPath, content, 0o600); err != nil {
		slog.Warn("compilation: writeFileIfChanged write failed", "path", path, "err", err)
		return fmt.Errorf("writing file %s: %w", path, err)
	}
	return nil
}

func removeMissingManagedSkillDirs(dst string, managedPrefix string, activeDirs map[string]struct{}) error {
	slog.Info("compilation: removeMissingManagedSkillDirs")
	entries, err := os.ReadDir(dst)
	if err != nil && !os.IsNotExist(err) {
		slog.Warn("compilation: removeMissingManagedSkillDirs ReadDir failed", "dst", dst, "err", err)
		return fmt.Errorf("read dir %s: %w", dst, err)
	}
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		if !strings.HasPrefix(entry.Name(), managedPrefix) {
			continue
		}
		if _, ok := activeDirs[entry.Name()]; ok {
			continue
		}
		if err := os.RemoveAll(filepath.Join(dst, entry.Name())); err != nil {
			return fmt.Errorf("removing directory %s: %w", filepath.Join(dst, entry.Name()), err)
		}
	}
	return nil
}

func removeMissingManagedFiles(dst string, ext string, activeFiles map[string]struct{}) error {
	slog.Info("compilation: removeMissingManagedFiles")
	entries, err := os.ReadDir(dst)
	if err != nil && !os.IsNotExist(err) {
		slog.Warn("compilation: removeMissingManagedFiles ReadDir failed", "dst", dst, "err", err)
		return fmt.Errorf("read dir %s: %w", dst, err)
	}
	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}
		if !strings.HasSuffix(entry.Name(), ext) {
			continue
		}
		if _, ok := activeFiles[entry.Name()]; ok {
			continue
		}
		path := filepath.Join(dst, entry.Name())
		content, readErr := os.ReadFile(path)
		if readErr != nil {
			continue
		}
		if !HasGeneratedMarker(string(content)) {
			continue
		}
		if err := os.Remove(path); err != nil {
			return fmt.Errorf("removing file %s: %w", path, err)
		}
	}
	return nil
}
