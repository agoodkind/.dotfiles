const (
	// AgentSourceDirName is the name of the agent source directory within dotfiles.
	AgentSourceDirName = ".agents"
	// CursorCompatibilityDirName is the legacy cursor directory name used as a compatibility symlink target.
	CursorCompatibilityDirName = ".cursor"
	// GeneratedAgentMarker is the plain-text marker embedded in files generated from .agents.
	GeneratedAgentMarker = "# Generated by dotfiles from .agents. Do not edit this file directly."
	// GeneratedAgentHTMLMarker is the HTML comment marker embedded in files generated from .agents.
	GeneratedAgentHTMLMarker = "<!-- Generated by dotfiles from .agents. Do not edit this file directly. -->"
	// GeneratedCursorMarker is the HTML comment marker embedded in files generated from .cursor.
	GeneratedCursorMarker = "<!-- Generated by dotfiles from .cursor. Do not edit this file directly. -->"
)

// AgentSourcePaths holds resolved paths for the agent source directory tree.
type AgentSourcePaths struct {
	Root     string
	Rules    string
	Commands string
	Skills   string
}

type agentRuleFrontmatter struct {
	Description string `yaml:"description,omitempty"`
	Globs       string `yaml:"globs,omitempty"`
	AlwaysApply bool   `yaml:"alwaysApply,omitempty"`
}

type frontmatterMetadata struct {
	Name        string `yaml:"name,omitempty"`
	Description string `yaml:"description,omitempty"`
	ApplyTo     string `yaml:"applyTo,omitempty"`
}

// ResolveAgentSource returns the AgentSourcePaths for the given dotfiles root.
func ResolveAgentSource(dotfiles string) AgentSourcePaths {
	root := filepath.Join(dotfiles, AgentSourceDirName)
	if _, err := os.Stat(root); err != nil {
		compatibilityRoot := filepath.Join(dotfiles, CursorCompatibilityDirName)
		if _, compatibilityErr := os.Stat(compatibilityRoot); compatibilityErr == nil {
			root = compatibilityRoot
		}
	}

	return AgentSourcePaths{
		Root:     root,
		Rules:    filepath.Join(root, "rules"),
		Commands: filepath.Join(root, "commands"),
		Skills:   filepath.Join(root, "skills"),
	}
}

// EnsureCursorCompatibilityLink ensures .cursor is a symlink pointing to .agents.
func EnsureCursorCompatibilityLink(dotfiles string) error {
	agentsRoot := filepath.Join(dotfiles, AgentSourceDirName)
	if _, err := os.Stat(agentsRoot); err != nil && !os.IsNotExist(err) {
		slog.Warn("compilation: stat failed", "path", agentsRoot, "err", err)
		return fmt.Errorf("stat %s: %w", agentsRoot, err)
	}

	cursorPath := filepath.Join(dotfiles, CursorCompatibilityDirName)
	info, err := os.Lstat(cursorPath)
	if err != nil {
		if os.IsNotExist(err) {
			if symlinkErr := os.Symlink(AgentSourceDirName, cursorPath); symlinkErr != nil {
				return fmt.Errorf("creating symlink %s: %w", cursorPath, symlinkErr)
			}
			return nil
		}
		return fmt.Errorf("stat %s: %w", cursorPath, err)
	}
	if info.Mode()&os.ModeSymlink == 0 {
		return fmt.Errorf("%s exists and is not a symlink", cursorPath)
	}

	linkTarget, err := os.Readlink(cursorPath)
	if err != nil {
		return fmt.Errorf("reading symlink %s: %w", cursorPath, err)
	}
	if !filepath.IsAbs(linkTarget) {
		linkTarget = filepath.Join(filepath.Dir(cursorPath), linkTarget)
	}
	if filepath.Clean(linkTarget) != filepath.Clean(agentsRoot) {
		return fmt.Errorf("%s points to %s, expected %s", cursorPath, linkTarget, agentsRoot)
	}
	return nil
}

// HasGeneratedMarker reports whether content contains any generated-file marker.
func HasGeneratedMarker(content string) bool {
	return strings.Contains(content, GeneratedAgentMarker) || strings.Contains(content, GeneratedAgentHTMLMarker) || strings.Contains(content, GeneratedCursorMarker)
}

// CompileZshFiles compiles all .zsh files found under standard dotfiles directories.
func CompileZshFiles(ctx context.Context, dotfiles string, logger *telemetry.Logger) error {
	dirs := []string{
		filepath.Join(dotfiles, "zshrc"),
		filepath.Join(dotfiles, "lib", "zinit"),
		filepath.Join(dotfiles, "lib", "zsh-defer"),
		filepath.Join(dotfiles, "home"),
		filepath.Join(dotfiles, "bin"),
	}
	for _, dir := range dirs {
		_ = filepath.WalkDir(dir, func(path string, entry os.DirEntry, err error) error {
			if err != nil {
				return err
			}
			if entry.IsDir() {
				return nil
			}
			name := filepath.Base(path)
			if !strings.HasSuffix(name, ".zsh") && name != ".zshrc" {
				return nil
			}
			compiled := path + ".zwc"
			if NeedsCompile(path, compiled) {
				_ = cmdexec.RunWithLogger(ctx, logger, "zsh", "-c", fmt.Sprintf("zcompile %q", path))
			}
			return nil
		})
	}
	return nil
}

// NeedsCompile reports whether source is newer than target and requires recompilation.
func NeedsCompile(source string, target string) bool {
	sourceInfo, err := os.Stat(source)
	if err != nil {
		return false
	}
	targetInfo, err := os.Stat(target)
	if err != nil {
		return true
	}
	return sourceInfo.ModTime().After(targetInfo.ModTime())
}

// RebuildZcompdump removes and regenerates the zsh completion dump file.
func RebuildZcompdump(ctx context.Context, dotfiles string, logger *telemetry.Logger) error {
	slog.InfoContext(ctx, "compilation: RebuildZcompdump")
	_ = os.RemoveAll(filepath.Clean(filepath.Join(os.Getenv("HOME"), ".zcompdump")))
	if !runner.HasCommand("zsh") {
		return nil
	}
	fpath := filepath.Join(dotfiles, "zshrc", "completions")
	zcompdump := "~/.zcompdump"
	autoloadCmd := fmt.Sprintf("fpath=(\"%s\" $fpath)\nautoload -Uz compinit\n", fpath)
	if err := cmdexec.RunWithLogger(ctx, logger, "zsh", "-c", autoloadCmd+"compinit -d "+zcompdump+"\nzcompile "+zcompdump+"\n"); err != nil {
		slog.ErrorContext(ctx, "rebuild zsh completions", "err", err)
		return fmt.Errorf("rebuild zsh completions: %w", err)
	}
	return nil
}

