-- Enable OSC 52 clipboard for SSH sessions
vim.g.clipboard = {
  name = 'OSC 52',
  copy = {
    ['+'] = require('vim.ui.clipboard.osc52').copy('+'),
    ['*'] = require('vim.ui.clipboard.osc52').copy('*'),
  },
  paste = {
    ['+'] = require('vim.ui.clipboard.osc52').paste('+'),
    ['*'] = require('vim.ui.clipboard.osc52').paste('*'),
  },
}

-- Use system clipboard
vim.opt.clipboard = 'unnamedplus'

-- Mouse support
vim.opt.mouse = 'a'
